def test_domain_separation_log_signature_not_replayable():
    w = new_ed25519()
    cp = make_checkpoint()
    signed = {k: v for k, v in cp.items() if k != "log_signature"}
    log_digest = hashlib.sha256(canonicalize(signed).encode("utf-8")).digest()  # NO domain tag
    log_sig = _b64u(w["priv"].sign(log_digest))
    forged = {"alg": WITNESS_VERSION, "witness_id": "witness-a", "tree_size": cp["tree_size"],
              "root_hash": cp["root_hash"], "log_key_id": cp["log_key_id"], "signature": log_sig}
    r = verify_witness_cosignature(cp, forged, {"witness_id": "witness-a", "public_key": w["pub"]})
    assert r["verified"] is False, "a log signature must not be replayable as a witness cosignature"


def test_witness_preimage_includes_domain_tag():
    cp = make_checkpoint()
    signed = dict(cp)
    witness_pre = WITNESS_DOMAIN_TAG.encode("utf-8") + canonicalize(signed).encode("utf-8")
    expected = hashlib.sha256(witness_pre).digest()
    assert witness_signing_digest(cp) == expected
    log_digest = hashlib.sha256(canonicalize(signed).encode("utf-8")).digest()
    assert witness_signing_digest(cp) != log_digest


def test_malformed_inputs_refuse():
    w = new_ed25519()
    cp = make_checkpoint()
    good = cosign(cp, "witness-a", w)
    pin = {"witness_id": "witness-a", "public_key": w["pub"]}
    assert verify_witness_cosignature(None, good, pin)["verified"] is False
    assert verify_witness_cosignature(cp, None, pin)["verified"] is False
    assert verify_witness_cosignature(cp, good, None)["verified"] is False
    assert verify_witness_cosignature(cp, good, {"witness_id": "witness-a"})["verified"] is False
    assert verify_witness_cosignature(cp, good, {"public_key": w["pub"]})["verified"] is False
    assert verify_witness_cosignature(cp, {**good, "signature": None}, pin)["verified"] is False
    assert verify_witness_cosignature(cp, {**good, "witness_id": None}, pin)["verified"] is False
    assert verify_witness_cosignature(cp, {**good, "alg": "WRONG"}, pin)["verified"] is False


# ── k-of-n quorum helper ─────────────────────────────────────────────────────
def test_quorum_accepts_k_distinct():
    wA, wB, wC = new_ed25519(), new_ed25519(), new_ed25519()
    cp = make_checkpoint()
    pinned = [{"witness_id": "a", "public_key": wA["pub"]},
              {"witness_id": "b", "public_key": wB["pub"]},
              {"witness_id": "c", "public_key": wC["pub"]}]
    cosigs = [cosign(cp, "a", wA), cosign(cp, "b", wB), cosign(cp, "c", wC)]
    r = require_witness_quorum(cp, cosigs, pinned, 2)
    assert r["ok"] is True
    assert r["met"] == 3
    assert r["required"] == 2
    assert r["witness_ids"] == ["a", "b", "c"]


def test_quorum_refuses_k_minus_1():
    wA, wB, wC = new_ed25519(), new_ed25519(), new_ed25519()
    cp = make_checkpoint()
    pinned = [{"witness_id": "a", "public_key": wA["pub"]},
              {"witness_id": "b", "public_key": wB["pub"]},
              {"witness_id": "c", "public_key": wC["pub"]}]
    cosigs = [cosign(cp, "a", wA), cosign(cp, "b", wB)]
    r = require_witness_quorum(cp, cosigs, pinned, 3)
    assert r["ok"] is False
    assert r["met"] == 2
    assert r["required"] == 3


def test_quorum_counts_duplicate_once():
    wA, wB = new_ed25519(), new_ed25519()
    cp = make_checkpoint()
    pinned = [{"witness_id": "a", "public_key": wA["pub"]},
              {"witness_id": "b", "public_key": wB["pub"]}]
    cosigs = [cosign(cp, "a", wA), cosign(cp, "a", wA)]
    r = require_witness_quorum(cp, cosigs, pinned, 2)
    assert r["ok"] is False
    assert r["met"] == 1
    assert r["witness_ids"] == ["a"]
    assert 'duplicate cosignature from witness "a"' in " ".join(r["reasons"])


