def test_tampered_checkpoint_refuses():
    w = new_ed25519()
    cp = make_checkpoint()
    cosig = cosign(cp, "witness-a", w)
    tampered = make_checkpoint(root_hash="sha256:" + "ff" * 32)
    bare = {k: v for k, v in cosig.items() if k != "root_hash"}
    r = verify_witness_cosignature(tampered, bare, {"witness_id": "witness-a", "public_key": w["pub"]})
    assert r["verified"] is False
    assert "does not verify over the checkpoint committed bytes" in r["reason"]


